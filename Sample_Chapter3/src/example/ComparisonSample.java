package example;

public class ComparisonSample {
    public static void main(String[] args) {
        // int number = 5;
        int number = 10;
        System.out.println(number == 10);
        System.out.println(number != 10);
        System.out.println(number > 10);
        System.out.println(number < 10);
        System.out.println(number >= 10);
        System.out.println(number <= 10);
    }
}
